        <h3>Before Interview</h3> 
        When going in to a internship interview you will have the opportunity to show your strengths, skills and knowledge. You need to use it well to captivate your interviewers and have a greater change of landing the internship.
        <br><br> Most often, it is an HR manager and / or head of department  who advises candidates to interview. The purpose is to see how the applicant is, whether he meets the criteria set and whether he/she fits to the employee/company group.
        People often get anxious to attend a internship interview and there is nothing abnormal about it, but you can significantly reduce your anxiety by preparing yourself. A good way to prepare yourself is to:
        <br><br> 
        <strong>- Get acquainted with the company,</strong><br>
        <strong> - Prepare what you're going to say,</strong><br> 
        <strong>- Choose a suitable outfit,</strong><br> 
        <strong>- Show interest.</strong>
